let da;
switch(new Date().getDay()){
    case 0 :
        da='Sunday'
        break;
        case 1:
            da='Monday'
            break;
            case 2 :
        da='Tuesday'
        break;
        case 3:
            da='Wednesday'
            break;
            case 4 :
        da='Thursday'
        break;
        case 5:
            da='Friday'
            break;
            case 6 :
        da='Saturday'
        break;
        default:
            da='No day displayed'
            break;
          
}
console.log('today is',da);
